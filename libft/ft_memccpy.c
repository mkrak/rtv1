#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t i;

	i = 0;
	while (i < n)
	{
		*(char *)dst++ = *(char *)src;
		if (*(char *)src++ == c)
			return (dst);
		i++;
	}
	return (NULL);
}
