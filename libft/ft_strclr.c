#include "libft.h"

void	ft_strclr(char *s)
{
	size_t size;
	size_t i;

	if (s)
	{
		size = ft_strlen(s);
		i = 0;
		while (i < size)
		{
			s[i] = 0;
			i++;
		}
	}
}
