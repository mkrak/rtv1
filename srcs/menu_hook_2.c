void	menu_hook_b(int k, int x, int y, t_control *l)
{
	if ((x >= (W + 45) + 16 && x <= (W + 45) + 48) && (y >= 570 && y <= 600)
		&& k == 1)
		l->coef->b -= 100;
	if ((x >= (W + 45) + 48 && x <= (W + 45) + 80) && (y >= 570 && y <= 600)
		&& k == 1)
		l->coef->b -= 10;
	if ((x >= (W + 45) + 80 && x <= (W + 45) + 112) && (y >= 570 && y <= 600)
		&& k == 1)
		l->coef->b -= 1;
	if ((x >= (W + 45) + 144 && x <= (W + 45) + 176) && (y >= 570 && y <= 600)
		&& k == 1)
		l->coef->b += 1;
	if ((x >= (W + 45) + 176 && x <= (W + 45) + 208) && (y >= 570 && y <= 600)
		&& k == 1)
		l->coef->b += 10;
	if ((x >= (W + 45) + 208 && x <= (W + 45) + 240) && (y >= 570 && y <= 600)
		&& k == 1)
		l->coef->b += 100;
}

